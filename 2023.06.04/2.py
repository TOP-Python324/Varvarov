n = int(input())
numbers_sum = 0

# ИСПОЛЬЗОВАТЬ: если переменная цикла не используется в теле цикла, то вместо неё пишем _
for _ in range(n):
    numbers = int(input())
    if numbers > 0:
        numbers_sum += numbers
print()
print(numbers_sum)


